use worker::*;

#[event(fetch)]
async fn fetch(
    _req: Request,
    _env: Env,
    _ctx: Context,
) -> Result<Response> {
    let html = r#"<!DOCTYPE html>
    <body>
        <h1>Hello World</h1>
        <p>This markup was generated by a Cloudflare Worker.</p>
    </body>
    "#;
    Response::from_html(html)
}
